package schemacrawler.test;

import static schemacrawler.test.utility.DatabaseTestUtility.getCatalog;

import java.io.IOException;
import java.sql.Connection;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.jdbc.datasource.embedded.EmbeddedDatabase;
import org.springframework.jdbc.datasource.embedded.EmbeddedDatabaseBuilder;

import schemacrawler.crawl.ForeignKeyRetrieverTest;
import schemacrawler.schema.Catalog;
import schemacrawler.schemacrawler.LoadOptionsBuilder;
import schemacrawler.schemacrawler.SchemaCrawlerOptions;
import schemacrawler.schemacrawler.SchemaCrawlerOptionsBuilder;
import schemacrawler.schemacrawler.SchemaInfoLevelBuilder;

public class ForeignKeySortTest {

  private Catalog catalog;

  @Test
  public void fkSort() throws IOException {
    ForeignKeyRetrieverTest.verifyRetrieveForeignKeys(catalog, "ForeignKeySortTest.fkSort");
  }

  @BeforeEach
  public void loadCatalog() throws Exception {

    final EmbeddedDatabase db =
        new EmbeddedDatabaseBuilder()
            .generateUniqueName(true)
            .setScriptEncoding("UTF-8")
            .ignoreFailedDrops(true)
            .addScript("fk_sort.sql")
            .build();

    final Connection connection = db.getConnection();

    final LoadOptionsBuilder loadOptionsBuilder =
        LoadOptionsBuilder.builder().withSchemaInfoLevel(SchemaInfoLevelBuilder.maximum());
    final SchemaCrawlerOptions schemaCrawlerOptions =
        SchemaCrawlerOptionsBuilder.newSchemaCrawlerOptions()
            .withLoadOptions(loadOptionsBuilder.toOptions());

    catalog = getCatalog(connection, schemaCrawlerOptions);
  }
}
